<h1 style="text-align: center;">Política de Privacidad</h1>

<p style="text-align: center; font-size: 18px;">Última actualización: [26/03/2024]</p>

<p>Esta Política de Privacidad describe cómo <strong>Colombia para la paz mundial</strong> ("nosotros", "nuestro", "nos") recopila, utiliza y protege la información personal que nos proporcionas cuando interactúas con nuestro formulario en línea ("Servicio").</p>

<h2>Recopilación de Información</h2>

<p>Podemos recopilar información personal identificable, incluidos pero no limitados a tu nombre, dirección de correo electrónico, número de teléfono y cualquier otra información que nos proporciones voluntariamente cuando utilices nuestro Servicio.</p>

<h2>Uso de la Información</h2>

<p>La información que recopilamos se utiliza para proporcionar y mejorar nuestro Servicio. Podemos utilizar tu información para responder a tus consultas, procesar transacciones, enviar correos electrónicos de seguimiento y proporcionarte información y actualizaciones relacionadas con nuestro Servicio.</p>

<h2>Protección de la Información</h2>

<p>Nos comprometemos a proteger la seguridad de tu información personal. Utilizamos medidas de seguridad razonables para proteger tu información contra el acceso no autorizado, la divulgación, la alteración o la destrucción.</p>

<h2>Divulgación a Terceros</h2>

<p>No compartiremos tu información personal con terceros sin tu consentimiento, excepto en los casos que se describen en esta Política de Privacidad o cuando lo exija la ley.</p>

<h2>Enlaces a Terceros</h2>

<p>Nuestro Servicio puede contener enlaces a sitios web de terceros. No somos responsables de las prácticas de privacidad ni del contenido de dichos sitios web. Te recomendamos que revises las políticas de privacidad de esos sitios web de terceros antes de proporcionarles tu información personal.</p>

<h2>Cambios en la Política de Privacidad</h2>

<p>Nos reservamos el derecho de actualizar o modificar esta Política de Privacidad en cualquier momento. Te notificaremos cualquier cambio significativo publicando la Política de Privacidad revisada en nuestro sitio web.</p>

