  <h1>Reglamento de Privacidad y Recopilación de Información</h1>
  
  <p>En Colombia para la paz mundial, nos tomamos muy en serio la privacidad y seguridad de la información personal de nuestros usuarios. A continuación, detallamos nuestro reglamento de privacidad y recopilación de información:</p>

  <h2>1. Consentimiento del Usuario</h2>

  <p>Al utilizar nuestros servicios en línea, el usuario acepta de manera voluntaria y explícita los términos y condiciones de nuestro reglamento de privacidad y recopilación de información.</p>

  <h2>2. Información Recopilada</h2>

  <p>Recopilamos información personal y sensible proporcionada por el usuario de forma voluntaria, como nombre, dirección de correo electrónico, número de teléfono y cualquier otra información necesaria para brindar nuestros servicios. También podemos recopilar información automáticamente a través de cookies y otras tecnologías de seguimiento.</p>

  <h2>3. Uso de la Información</h2>

  <p>La información recopilada se utiliza para proporcionar y mejorar nuestros servicios, así como para comunicarnos con el usuario, procesar transacciones y cumplir con las obligaciones legales. No compartiremos ni venderemos la información personal del usuario a terceros sin su consentimiento explícito, excepto en los casos que lo exija la ley.</p>

  <h2>4. Seguridad de la Información</h2>

  <p>Implementamos medidas de seguridad razonables para proteger la información personal del usuario contra el acceso no autorizado, la divulgación, la alteración o la destrucción. Sin embargo, no podemos garantizar la seguridad absoluta de la información transmitida a través de internet o almacenada en nuestros sistemas.</p>

  <h2>5. Derechos del Usuario</h2>

  <p>El usuario tiene derecho a acceder, corregir, actualizar o eliminar su información personal en cualquier momento. También puede optar por no proporcionar cierta información, aunque esto puede limitar su capacidad para utilizar ciertas funciones de nuestros servicios.</p>

  <p>Al utilizar nuestros servicios, el usuario reconoce y acepta los términos y condiciones de nuestro reglamento de privacidad y recopilación de información. Si tiene alguna pregunta o inquietud, no dude en ponerse en contacto con nosotros.</p>
